class LetterComparison {
    public static void main(String[] args) {
        
        char letterA = 'A';
        char letterB = 'B';
        
    
        System.out.println("Comparison between 'A' and 'B':");
        System.out.println("'A' < 'B' : " + (letterA < letterB)); 
        System.out.println("'A' <= 'B' : " + (letterA <= letterB));  
        System.out.println("'A' > 'B' : " + (letterA > letterB)); 
        System.out.println("'A' >= 'B' : " + (letterA >= letterB));  
        System.out.println("'A' == 'B' : " + (letterA == letterB));  
        System.out.println("'A' != 'B' : " + (letterA != letterB));  
    }
}
